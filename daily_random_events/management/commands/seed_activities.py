for index, payload in enumerate(SEED_ACTIVITIES):
    aliases = [payload["title"]]
    if index < len(LEGACY_TITLES):
        aliases.append(LEGACY_TITLES[index])
    payload["_lookup_titles"] = aliases


class Command(BaseCommand):
    help = "Seed starter activities for the daily random events app."

    def handle(self, *args, **options):
        created_count = 0
        updated_count = 0

        for payload in SEED_ACTIVITIES:
            lookup_titles = payload["_lookup_titles"]
            defaults = {
                key: value for key, value in payload.items() if not key.startswith("_")
            }

            existing = (
                Activity.objects.filter(title__in=lookup_titles).order_by("id").first()
            )

            if existing:
                for field, value in defaults.items():
                    setattr(existing, field, value)
                existing.save()
                updated_count += 1
                continue

            Activity.objects.create(**defaults)
            created_count += 1

        self.stdout.write(
            self.style.SUCCESS(
                f"Seed complete: {created_count} created, {updated_count} updated."
            )
        )
