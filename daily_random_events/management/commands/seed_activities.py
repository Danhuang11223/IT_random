def build_activity(
    title,
    description,
    *,
    category,
    min_time,
    max_time,
    min_budget,
    max_budget,
    moods,
    social_type,
    is_outdoor=False,
):
    return {
        "title": title,
        "description": description,
        "category": category,
        "min_time_minutes": min_time,
        "max_time_minutes": max_time,
        "min_budget": f"{int(min_budget)}.00",
        "max_budget": f"{int(max_budget)}.00",
        "mood_tags": list(moods),
        "social_type": social_type,
        "is_outdoor": is_outdoor,
        "is_active": True,
    }


